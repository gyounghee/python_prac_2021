# 내 답안
n = [input().split() for _ in range(10)]
stop, l2 = 0, 1
for i in range(1,len(n)):
    if stop == 1 : break
    for j in range(l2,len(n)):
        if n[i][j] == '0': n[i][j] = '9'
        if n[i][j] == '1' :
            l2 = j-1
            break
        if n[i][j] == '2' :
            n[i][j] = '9'
            stop = 1
            break
for x in range(len(n)):
    for y in range(len(n)):
        print(n[x][y],end=" ")
    print()
